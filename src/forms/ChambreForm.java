    private void loadCategories(){
        categorieList.removeAllItems();
        for (Categorie c : gs.findAll()){
            categorieList.addItem(c);
        }
    }
    
    private void load(){
        tm.setRowCount(0);
        for(Chambre c:chs.findAll()){
            tm.addRow(new Object[]{
                c.getId(),
                c.getTele(),
                c.getCat().getLibelle(),
            });
        }
    }
    
