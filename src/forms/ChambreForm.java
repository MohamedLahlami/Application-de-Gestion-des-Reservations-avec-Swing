    private void chambreTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_chambreTableMouseClicked
        int id = Integer.parseInt(tm.getValueAt(chambreTable.getSelectedRow(), 0).toString());
        currentChambre = chs.findById(id);
        teleInput.setText(currentChambre.getTele());
        categorieList.setSelectedItem(currentChambre.getCat());
    }//GEN-LAST:event_chambreTableMouseClicked

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if (teleInput.getText() == ""){
            JOptionPane.showMessageDialog(this, "veuillez saisir le libellé de la chambre", "Erreur", ERROR_MESSAGE);
        }else{
            chs.create(new Chambre(teleInput.getText(), (Categorie)categorieList.getSelectedItem()));
            JOptionPane.showMessageDialog(this, "Chambre bien Ajoutée");
            clearButton.doClick();
            load();
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if (currentChambre == null){
            JOptionPane.showMessageDialog(this, "Veuillez saisir une ligne de la table", "Erreur", ERROR_MESSAGE);
        }else{
            int choice = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment supprimer la chambre " + currentChambre.getId() + "?");
            if (choice == 0) {
                if(chs.delete(currentChambre.getId())){
                    JOptionPane.showMessageDialog(this, "Chambre supprimée");
                    clearButton.doClick();
                    load();
                }
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        if (currentChambre == null){
            JOptionPane.showMessageDialog(this, "Veuillez saisir une ligne de la table", "Erreur", ERROR_MESSAGE);
        }else{
            if(chs.update(new Chambre(currentChambre.getId(), teleInput.getText(), (Categorie)categorieList.getSelectedItem()))){
                JOptionPane.showMessageDialog(this, "Chambre bien modifiée");
                clearButton.doClick();
                load();
            }
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void clearButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clearButtonActionPerformed
        teleInput.setText("");
        categorieList.setSelectedIndex(0);
        currentChambre = null;
    }//GEN-LAST:event_clearButtonActionPerformed


